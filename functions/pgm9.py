#simple addition program with Def function

def sum2 (a,b):
    return a+b

result=sum2(4,7)
print(result)

#simple multiplication program with def function

def mul3 (num):
    return num*3

result=mul3(9)
print(result)

#lambda use to define the program

final_result=lambda num:num*3
print(final_result(9))